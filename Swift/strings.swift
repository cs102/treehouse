import UIKit

println("List:\n 1 - Apple 2 - Bananas 6 - Oranges")
//prints list in console in a new line
//eg:
//List:
//1 - Apple 2 - Bananas 6 - Oranges


// string literals
// \t : new tab
// \n : new line
//unicode : world wide standard to consistency represent characters
//unicode : supports any foreign language including emoji


println("List:\n 1 - Apple\n 2 - Bananas\n 6 - Oranges")
// \n : new line
//Console output
//List:
// 1 - Apple
// 2 - Bananas
// 6 - Oranges

println("List:\n 1  \tApple\n 2 \t Bananas\n 6 \t Oranges")
// \t : tabs
// Console output
//List:
// 1	Apple
// 2	Bananas
// 6	Oranges

